package entity;

public class Teacher {
	private String tid;
	private String tname;
	private String toffice;
	private String tphone;
	private String tqq;
	private boolean tischecker;
	public String getTid() {
		return tid;
	}
	public void setTid(String tid) {
		this.tid = tid;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public String getToffice() {
		return toffice;
	}
	public void setToffice(String toffice) {
		this.toffice = toffice;
	}
	public String getTphone() {
		return tphone;
	}
	public void setTphone(String tphone) {
		this.tphone = tphone;
	}
	public String getTqq() {
		return tqq;
	}
	public void setTqq(String tqq) {
		this.tqq = tqq;
	}
	public boolean isTischecker() {
		return tischecker;
	}
	public void setTischecker(boolean tischecker) {
		this.tischecker = tischecker;
	}
}
