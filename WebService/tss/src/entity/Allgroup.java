package entity;

public class Allgroup {
	private int gid;
	private String gleader;
	private int gsum;
	
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public String getGleader() {
		return gleader;
	}
	public void setGleader(String gleader) {
		this.gleader = gleader;
	}
	public int getGsum() {
		return gsum;
	}
	public void setGsum(int gsum) {
		this.gsum = gsum;
	}
}
